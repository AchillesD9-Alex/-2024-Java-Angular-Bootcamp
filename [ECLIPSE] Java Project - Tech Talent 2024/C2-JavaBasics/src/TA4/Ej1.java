package TA4;

public class Ej1 {

	public static void main(String[] args) {
		/* Declara dos variables numéricas (con el valor que desees), muestra por consola la suma,
		resta, multiplicación, división y módulo (resto de la división).*/
	int a=10, b=5;
	int suma=a+b;
	int resta=a-b;
	int mult=a*b;
	int div=a/b;
	int modulo=a%b;
	
	System.out.println("El valor de la primera variable es " +a);
	System.out.println("El valor de la segunda variable es " +b);
	System.out.println("El valor de la suma es: " +suma);
	System.out.println("El valor de la resta es: " +resta);
	System.out.println("El valor de la multiplicación es: " +mult);
	System.out.println("El valor de la división es: " +div);
	System.out.println("El valor del modulo es: " +modulo);
	System.out.println("FIN");
	
	}

}
